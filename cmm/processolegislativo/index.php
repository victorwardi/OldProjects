                            <p align="justify"><strong>O que &eacute; Processo Legislativo </strong><br />
                              <br />
                              No Brasil, em virtude de sua estrutura federal, da separa&ccedil;&atilde;o  dos Poderes e do regime presidencialista, as compet&ecirc;ncias foram claramente  repartidas e demarcadas pela Constitui&ccedil;&atilde;o Federal. Essa Lei Maior atribuiu,  predominantemente, mas n&atilde;o exclusivamente, a fun&ccedil;&atilde;o de administrar ao Poder  Executivo, a fun&ccedil;&atilde;o de julgar ao Poder Judici&aacute;rio e a fun&ccedil;&atilde;o de produzir e  aprovar leis ao Poder Legislativo. Dividiu tamb&eacute;m as atividades governamentais  entre as tr&ecirc;s esferas de nossa Federa&ccedil;&atilde;o, reservando &agrave; Uni&atilde;o, aos Estados, ao  Distrito Federal e aos Munic&iacute;pios responsabilidades diversas. Assim, ao  Munic&iacute;pio, em linhas gerais, foram reservados os assuntos predominantemente  ligados ao interesse local.<br />
                              <br />
                              No &acirc;mbito do Munic&iacute;pio, o Poder Legislativo &eacute; exercido pela C&acirc;mara Municipal,  cujas fun&ccedil;&otilde;es principais s&atilde;o justamente legislar e fiscalizar a Administra&ccedil;&atilde;o  P&uacute;blica Municipal, a cargo do Poder Executivo.<br />
                              <br />
                              Essa C&acirc;mara, composta pelos Senhores vereadores, legisla propondo e/ou  aprovando projetos relativos ao interesse local e que devem passar por um  procedimento espec&iacute;fico, o processo legislativo. <br />
                              <br />
                              Assim, o processo legislativo &eacute; o conjunto de atos, ordenados na forma  estabelecida pela Constitui&ccedil;&atilde;o Federal e de acordo com seus princ&iacute;pios,  destinados a produzir normas jur&iacute;dicas de natureza legislativa, isto &eacute;, que  tramitam necessariamente pelo Poder Legislativo. <br />
                              <br />
                              No plano Municipal, de acordo com a Lei Org&acirc;nica do Munic&iacute;pio de Mangaratiba,  essas normas s&atilde;o as seguintes: as Emendas &agrave; pr&oacute;pria Lei Org&acirc;nica, as Leis, as  Resolu&ccedil;&otilde;es e os Decretos Legislativos.<br />
                              <br />
                              O projeto de emenda &agrave; Lei Org&acirc;nica visa alterar essa lei fundamental na qual se  baseia a organiza&ccedil;&atilde;o pol&iacute;tica do Munic&iacute;pio.<br />
                              <br />
                              Os projetos de resolu&ccedil;&otilde;es e decretos legislativos, de iniciativa exclusiva da  C&acirc;mara e que s&oacute; por ela tramitam, destinam-se, os primeiros a disciplinar  mat&eacute;ria dirigida ao &acirc;mbito interno do Legislativo, os segundos, a regrar  mat&eacute;ria privativa do Legislativo, com repercuss&atilde;o no exterior dele. S&atilde;o  exemplos, num caso, a aprova&ccedil;&atilde;o do Regimento Interno da C&acirc;mara; em outro, a  concess&atilde;o de t&iacute;tulos honor&iacute;ficos e honrarias. Os projetos de lei s&atilde;o aqueles  voltados para a cria&ccedil;&atilde;o de norma de car&aacute;ter geral, fruto da colabora&ccedil;&atilde;o entre o  Legislativo e o Executivo.<br />
                              <br />
                              No caso da iniciativa das leis, a regra geral &eacute; elas serem apresentadas,  indistintamente, dentro de certas condi&ccedil;&otilde;es, seja pelo Chefe do Executivo, seja  por membro ou &oacute;rg&atilde;o do Legislativo, ou mesmo pelos cidad&atilde;os, atrav&eacute;s de  iniciativa popular.<br />
                              <br />
                              Ocorre, entretanto, que a Constitui&ccedil;&atilde;o Federal e a Lei Org&acirc;nica estabelecem  exce&ccedil;&otilde;es que restringem significativamente a iniciativa das leis pelo  Legislativo. Cite-se, por exemplo, o fato de ser do Poder Executivo a  iniciativa de leis que disponham sobre organiza&ccedil;&atilde;o administrativa e servi&ccedil;o  p&uacute;blico.<br />
                              <br />
                              Todos os projetos s&atilde;o apresentados em plen&aacute;rio, tornados p&uacute;blicos pela leitura  e pela publica&ccedil;&atilde;o no Di&aacute;rio Oficial, sendo ent&atilde;o enviados &agrave;s Comiss&otilde;es  Permanentes da C&acirc;mara. A Comiss&atilde;o de Constitui&ccedil;&atilde;o e Justi&ccedil;a, a primeira a  estudar o projeto, far&aacute; o seu controle pr&eacute;vio, manifestando-se atrav&eacute;s de  parecer sobre sua constitucionalidade e legalidade. As outras Comiss&otilde;es,  chamadas Comiss&otilde;es de M&eacute;rito, opinar&atilde;o quanto ao conte&uacute;do do projeto, se ele &eacute;  bom ou n&atilde;o, se merece ou n&atilde;o ser aprovado.<br />
                              <br />
                              Durante a tramita&ccedil;&atilde;o nas Comiss&otilde;es poder&atilde;o ser realizadas audi&ecirc;ncias p&uacute;blicas,  por mandamento legal ou a pedido de entidades e dos cidad&atilde;os, para manifesta&ccedil;&atilde;o  dos especialistas e interessados sobre os projetos em an&aacute;lise.<br />
                              <br />
                              Enviados ao Plen&aacute;rio, os projetos ser&atilde;o objeto de um ou dois turnos de vota&ccedil;&atilde;o,  de acordo com sua esp&eacute;cie, e submetidos, conforme a mat&eacute;ria, a diferentes tipos  de quorum. Em uma ordem crescente de complexidade e de necessidade de maior  consenso, os projetos, com base na mat&eacute;ria de que tratam, ser&atilde;o submetidos,  para aprova&ccedil;&atilde;o (vota&ccedil;&atilde;o).<br />
                              <br />
                              Aprovados, os projetos de emenda &agrave; Lei Org&acirc;nica, de resolu&ccedil;&atilde;o e de decreto  legislativo ser&atilde;o promulgados e publicados, passando, se existe previs&atilde;o no  caso de vig&ecirc;ncia imediata, a vigorar em seguida.<br />
                              <br />
                              J&aacute; os projetos de lei, ap&oacute;s aprovados s&atilde;o enviados ao  Prefeito Municipal, que os sancionar&aacute;, colocando sua concord&acirc;ncia, ou os  vetar&aacute;, explicando os motivos jur&iacute;dicos e de interesse p&uacute;blico que o levaram a  negar seu aval ao projeto. Se o projeto de lei for vetado, total ou  parcialmente, ele retornar&aacute; &agrave; C&acirc;mara, que poder&aacute; concordar com o Chefe do  Executivo e mandar arquiv&aacute;-lo, ou derrubar o veto pela vota&ccedil;&atilde;o de maioria  absoluta de seus membros.<br />
                              <br />
                              Acrescente-se que, seja nas Comiss&otilde;es, seja nas discuss&otilde;es em Plen&aacute;rio, poder&atilde;o  ser apresentados, discutidos e aprovados substitutivos ou emendas, ou seja,  propostas alternativas ou pontuais, ao projeto original.<br />
                              <br />
                              Eis, em linhas gerais, a din&acirc;mica do processo legislativo na C&acirc;mara Municipal  de Mangaratiba.</p>
