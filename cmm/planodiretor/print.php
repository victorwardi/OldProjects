          <p class="txt_01"><strong>O que &eacute; Plano Diretor?</strong><br />
            <br />
O Plano Diretor &eacute; um instrumento para garantir a todos os   cidad&atilde;os do Munic&iacute;pio um lugar adequado para morar, trabalhar e viver com   dignidade, proporcionando acesso &agrave; habita&ccedil;&atilde;o adequada, saneamento ambiental, ao   transporte e mobilidade, ao tr&acirc;nsito seguro e aos servi&ccedil;os e equipamentos   urbanos. &Eacute; o que determina a Constitui&ccedil;&atilde;o Federal e o Estatuto das Cidades.<br />
<br />
Segundo o Estatuto das Cidades (Lei n.&ordm; 10.257/2001), os   munic&iacute;pios devem planejar seu desenvolvimento por meio de um Plano Diretor. Esse   planejamento precisa ser conduzido pela Prefeitura, aprovado pela c&acirc;mara de   vereadores e contar com o envolvimento de toda a sociedade em sua elabora&ccedil;&atilde;o e   implementa&ccedil;&atilde;o.<br />
<br />
<br />
<strong>Por Que Fazer Um Plano Diretor?</strong><br />
<br />
Para construir solu&ccedil;&otilde;es para os problemas que afligem os   moradores das cidades brasileiras, que cresceram de forma excludente e   desequilibrada, penalizando principalmente a popula&ccedil;&atilde;o mais pobre. A realiza&ccedil;&atilde;o   do Plano Diretor pelos munic&iacute;pios pode contribuir tamb&eacute;m para a constru&ccedil;&atilde;o da   pol&iacute;tica nacional de desenvolvimento urbano.<br />
<br />
O Plano Diretor &eacute; elaborado para uma perspectiva de m&eacute;dio   prazo, estando sujeito a avalia&ccedil;&otilde;es peri&oacute;dicas, sem prazos definidos, mas sempre   que fatos significativos e fen&ocirc;menos urbanos o requeiram.<br />
<br />
<br />
<strong>Quem Faz Um Plano Diretor?</strong><br />
<br />
Uma equipe da Prefeitura , em conjunto com os representantes da   sociedade, conduz o Plano Diretor por meio de um processo democr&aacute;tico e   participativo. Para que o plano se torne realidade e seja um instrumento eficaz   de gest&atilde;o e planejamento, &eacute; preciso que ele seja pactuado por todos os segmentos   da sociedade e respaldado por uma ampla discuss&atilde;o no munic&iacute;pio.<br />
<br />
Neste contexto, &eacute; fundamental a participa&ccedil;&atilde;o dos diversos   setores da sociedade civil e das esferas de governo: t&eacute;cnicos da administra&ccedil;&atilde;o   municipal, &oacute;rg&atilde;os p&uacute;blicos estaduais, federais, representantes de universidades   p&uacute;blicas e privadas, movimentos populares, representantes de associa&ccedil;&otilde;es de   bairros, al&eacute;m de empres&aacute;rios de v&aacute;rios setores da produ&ccedil;&atilde;o.<br />
<br />
<br />
<strong>O Que Se Espera do Plano Diretor?</strong><br />
<br />
&bull; Que proponha meios para garantir e incentivar a participa&ccedil;&atilde;o   popular na gest&atilde;o do munic&iacute;pio.<br />
<br />
&bull; Que aponte rumos para um desenvolvimento local   economicamente vi&aacute;vel, socialmente justo e ecologicamente equilibrado.<br />
<br />
&bull; Que proponha solu&ccedil;&otilde;es para a melhoria da qualidade da gest&atilde;o   p&uacute;blica local, tornando-a mais apta a utilizar os recursos p&uacute;blicos.<br />
<br />
&bull; Que apresente diretrizes e instrumentos para que os   investimentos em saneamento, transporte coletivo, sa&uacute;de, educa&ccedil;&atilde;o, equipamentos   urbanos, habita&ccedil;&atilde;o popular sejam adequadamente distribu&iacute;dos e beneficiem toda a   popula&ccedil;&atilde;o e a prestar melhores servi&ccedil;os &agrave; popula&ccedil;&atilde;o.<br />
<br />
&bull;&nbsp; Que proponha diretrizes para proteger o meio ambiente, os   mananciais, as &aacute;reas verdes e o patrim&ocirc;nio hist&oacute;rico local.<br />
          <br />
          <br />
          <strong>Planejamento Estrat&eacute;gico</strong><br />
          <br />
Planejamento Estrat&eacute;gico &eacute; um m&eacute;todo ou processo que <strong>consiste em prever, identificar e mobilizar potenciais dispon&iacute;veis e   condi&ccedil;&otilde;es favor&aacute;veis, tendo por objetivo direcionar a&ccedil;&otilde;es t&aacute;ticas, definir   estrat&eacute;gias e implementar programas e projetos, visando &agrave; obten&ccedil;&atilde;o de resultados   desej&aacute;veis para o futuro de uma cidade.</strong><br />
<br />
Um Planejamento Estrat&eacute;gico implica um compromisso coletivo com   um processo cont&iacute;nuo de adapta&ccedil;&atilde;o e mudan&ccedil;a, num esfor&ccedil;o ordenado para produzir   a&ccedil;&otilde;es decis&oacute;rias que orientem a cidade, a partir do est&aacute;gio em que se encontra ,   em dire&ccedil;&atilde;o a um objetivo central<em>. </em>A sociedade que n&atilde;o sabe para   onde vai n&atilde;o chega a lugar algum.<br />
<br />
&bull;&nbsp; Atrav&eacute;s do Planejamento Estrat&eacute;gico, ser&aacute; feito um   diagn&oacute;stico abrangente de Mangaratiba, seja nos &oacute;rg&atilde;os da administra&ccedil;&atilde;o p&uacute;blica   quanto nas regi&otilde;es do munic&iacute;pio. A metodologia constitui em identificar as   tend&ecirc;ncias globais e locais de cada aspectos dos temas cr&iacute;ticos e coteja-las com   a situa&ccedil;&atilde;o do munic&iacute;pio, sob o enfoque de pontos fortes e pontos fracos. Ou   seja, potencialidades de Mangaratiba em rela&ccedil;&atilde;o &agrave;s tend&ecirc;ncias que devem ser   desenvolvidas, ou debilidades que devem ser minimizadas para que o munic&iacute;pio se   desenvolva sob a perspectiva do futuro.<br />
<br />
<br />
<strong>Foco</strong><br />
<br />
&bull;&nbsp; Planejamento Estrat&eacute;gico Municipal &reg; Objetivos da   Administra&ccedil;&atilde;o e do Munic&iacute;pio e integra&ccedil;&atilde;o com os demais planejamentos.<br />
<br />
&bull;&nbsp; Plano Diretor Municipal &reg; Desenvolvimento e expans&atilde;o   territorial<br />
<br />
&bull;&nbsp; Planejamento Plurianual &reg; Planejamento Or&ccedil;ament&aacute;rio<br />
<br />
Um Planejamento Estrat&eacute;gico Municipal envolve   governo e sociedade numa interface de co-responsabilidade pela   defini&ccedil;&atilde;o de novos rumos. Este tipo de Planejamento diferencia-se do   Planejamento Tradicional por admitir incertezas, descontinuidades, vari&aacute;veis   imprevis&iacute;veis e novas tend&ecirc;ncias, mas acoplando ferramentas e t&eacute;cnicas   inovadoras torna-se um m&eacute;todo permanente de elabora&ccedil;&atilde;o, implementa&ccedil;&atilde;o, avalia&ccedil;&atilde;o   e mais confi&aacute;vel, pois &eacute; acima de tudo flex&iacute;vel.<br />
<br />
<br />
<strong>Resumo</strong><br />
<br />
&bull;&nbsp; Um impacto de consenso entre agentes p&uacute;blicos, privados e   cidad&atilde;os em prol de transforma&ccedil;&otilde;es que beneficiem a todos;<br />
<br />
&bull;&nbsp; Uma metodologia de transforma&ccedil;&atilde;o da cultura   urbana, que permite superar m&eacute;todos impositivos por novas formas de antecipa&ccedil;&atilde;o   do futuro desejado e poss&iacute;vel;<br />
<br />
&bull;&nbsp; Um desafio que consiste em definir o futuro desej&aacute;vel e os   meios reais para alcan&ccedil;a-lo;<br />
<br />
&bull;&nbsp; Um compromisso com a a&ccedil;&atilde;o.<br />
